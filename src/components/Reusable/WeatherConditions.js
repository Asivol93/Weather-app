export const WeatherConditions = (weather) => {
  if (weather === 'Clouds') {
    return 'U+1F60'
  } else if (weather === 'Rain' || weather === 'Drizzle') {
    return 'U+1F60'
  } else if (weather === 'Snow') {
    return 'U+1F60'
  } else if (weather === 'Clear') {
    return 'U+1F60'
  } else {
    return 'U+1F60'
  }
}
